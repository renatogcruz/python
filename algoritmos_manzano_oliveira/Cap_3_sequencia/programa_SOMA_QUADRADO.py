# Entrada
a = int(input("Entre com o primeiro valor: "))
b = int(input("Entre com o segundo valor: "))
c = int(input("Entre com o terceiro valor: "))

# Processamento
r = (a + b + c) ** 2

# Exbição
print(f"O quadrado da soma é {r}")


